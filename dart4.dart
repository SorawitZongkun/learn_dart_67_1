// Dynamic Variable Type
void main() {
  dynamic dynamicVariable = 42; // Initially an integer
  print(dynamicVariable); // Output: 42

  dynamicVariable = "Hello, Dart!"; // Now a string
  print(dynamicVariable); // Output: Hello, Dart!

  dynamicVariable = [1, 2, 3]; // Now a list
  print(dynamicVariable); // Output: [1, 2, 3]
}
